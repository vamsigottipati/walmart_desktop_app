function icon(name, size = 20, extraClass = '') {
  const inner = PATHS[name]
  if (!inner) return ''
  const cls = extraClass ? ` ${extraClass}` : ''
  const spin = name === 'loader' ? ' animate-spin' : ''
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" class="${cls}${spin}" aria-hidden="true">${inner}</svg>`
}

function replaceIcons(root = document) {
  root.querySelectorAll('[data-icon]').forEach((el) => {
    const name = el.getAttribute('data-icon')
    const size = parseInt(el.getAttribute('data-icon-size') || '20', 10)
    const svg = icon(name, size)
    if (svg) {
      el.innerHTML = svg
      el.removeAttribute('data-icon')
    }
  })
}

